def check_contain_value(first_list, second_list):
    if 3 in first_list:
        print('True')
    else:
        print('False')

    if -1 in second_list:
        print('True')
    else:
        print('False')


first_list = [1, 5, 8, 3]
second_list = [1, 5, 8, 3]
check_contain_value(first_list, second_list)

